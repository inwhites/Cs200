u8 SPI2_ReadWriteByte(u8 TxData)                                        //SPI读写数据函数
{		
	u8 retry=0;				 	
	/* Loop while DR register in not emplty */
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET)      //发送缓存标志位为空
		{
		retry++;
		if(retry>200)return 0;
		}			  
	/* Send byte through the SPI1 peripheral */
	SPI_I2S_SendData(SPI2, TxData);                                    //通过外设SPI1发送一个数据
	retry=0;
	/* Wait to receive a byte */
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_RXNE) == RESET);   //接收缓存标志位不为空
		{
		retry++;
		if(retry>200)return 0;
		}	  						    
	/* Return the byte read from the SPI bus */
	return SPI_I2S_ReceiveData(SPI2);                                 //通过SPI1返回接收数据				    
}


void optical_write(u8 add,u8 data)
{
	ClrNCS;
	Delay_Us(80);
	SPI2_ReadWriteByte(add+0x80);
	Delay_Us(80);
	SPI2_ReadWriteByte(data);
	Delay_Us(80);
	SetNCS;
}

u8 optical_read(u8 add)
{
	tp=0;
	ClrNCS;
	Delay_Us(80);
	tp=SPI2_ReadWriteByte(add);
	Delay_Us(80);
	tp=SPI2_ReadWriteByte(0xff);
	Delay_Us(80);
	SetNCS;
	return tp;
}

